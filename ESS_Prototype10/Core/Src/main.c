int8_t user_i2c_read(uint8_t reg_addr, uint8_t *data, uint32_t len, void *intf_ptr) {
    // Reads a register from BME680 using HAL I2C
    return HAL_I2C_Mem_Read((I2C_HandleTypeDef*)intf_ptr, BME68X_I2C_ADDR << 1, reg_addr,
                            I2C_MEMADD_SIZE_8BIT, data, len, HAL_MAX_DELAY) == HAL_OK ? 0 : -1;
}

int8_t user_i2c_write(uint8_t reg_addr, const uint8_t *data, uint32_t len, void *intf_ptr) {
    // Writes a register to BME680 using HAL I2C
    return HAL_I2C_Mem_Write((I2C_HandleTypeDef*)intf_ptr, BME68X_I2C_ADDR << 1, reg_addr,
                             I2C_MEMADD_SIZE_8BIT, (uint8_t*)data, len, HAL_MAX_DELAY) == HAL_OK ? 0 : -1;
}

void user_delay_us(uint32_t period, void *intf_ptr) {
    // Simple busy-wait for crude microsecond delay (good enough for sensor)
    for (volatile uint32_t i = 0; i < period * 10; i++) { __NOP(); }
}





