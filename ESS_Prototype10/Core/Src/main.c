/* ============================================================================
 * UART Receive Interrupt Handler - builds up a user command line
 * ========================================================================= */
volatile uint8_t cmd_ready = 0; // Global flag: set to 1 when a complete command is received

void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart) {
    // Check if the interrupt is from USART2 (our UART for CLI)
    if (huart->Instance == USART2) {
        // Check if received byte is a line ending (Enter key)
        if (rx_byte == '\r' || rx_byte == '\n') {
            // Only process if command buffer has content
            if (cmd_index > 0) {
                command_buffer[cmd_index] = '\0';  // Null-terminate the command string
                cmd_ready = 1;                     // Set flag to signal command is ready for processing
                cmd_index = 0;                     // Reset buffer index for next command
            }
        } else {
            command_buffer[cmd_index++] = rx_byte; // Add byte to buffer and increment index
            if (cmd_index >= sizeof(command_buffer))
                cmd_index = 0; // Prevent buffer overflow: reset if too long
        }
        HAL_UART_Receive_IT(&huart2, &rx_byte, 1); // Restart UART interrupt to keep receiving
    }
}



