// Activates the chip select pin for LoRa SPI (NSS LOW = active)
void LoRa_Select(void) {
    HAL_GPIO_WritePin(LORA_NSS_GPIO_Port, LORA_NSS_Pin, GPIO_PIN_RESET);
}

// Deactivates the chip select pin for LoRa SPI (NSS HIGH = inactive)
void LoRa_Unselect(void) {
    HAL_GPIO_WritePin(LORA_NSS_GPIO_Port, LORA_NSS_Pin, GPIO_PIN_SET);
}

// Waits until LoRa's BUSY pin goes low (module is ready for next SPI op)
void LoRa_WaitWhileBusy(void) {
    uint32_t timeout = HAL_GetTick() + 1000;  // 1 second timeout
    while (HAL_GPIO_ReadPin(LORA_BUSY_GPIO_Port, LORA_BUSY_Pin) == GPIO_PIN_SET) {
        if (HAL_GetTick() > timeout) {
            printf("[ERROR] BUSY timeout!\r\n");
            break; // exit while loop if stuck
        }
    }
}

// Issues a hardware reset pulse to the LoRa HAT (required for init)
void LoRa_Reset(void) {
    HAL_GPIO_WritePin(LORA_RST_GPIO_Port, LORA_RST_Pin, GPIO_PIN_RESET); // Set RESET low
    HAL_Delay(10);                                                       // Wait 10ms
    HAL_GPIO_WritePin(LORA_RST_GPIO_Port, LORA_RST_Pin, GPIO_PIN_SET);   // Set RESET high
    HAL_Delay(10);                                                       // Wait again
}


// Fully initialize/configure the SX1262 LoRa module.
void LoRa_Init(void) {
    LoRa_SetStandby();            // Standby mode (required for config)
    LoRa_SetPacketType();         // Set to LoRa packet type
    LoRa_SetRfFrequency(868000000); // Set EU ISM band, adjust if needed
    LoRa_SetBufferBaseAddress();  // TX/RX buffer address to 0
    LoRa_SetModulationParams();   // SF7/BW125/CR4_5
    LoRa_SetPacketParams(16);     // Default 16-byte payload, adjust as needed
    LoRa_SetTxParams();           // Output power, ramp time
    printf("LoRa: Module initialized.\r\n");
}

// Print the status of the SX126x chip.
void LoRa_PrintStatus(void) {
    uint8_t status = LoRa_GetStatus();
    printf("SX126x Status: 0x%02X\r\n", status);
}


// Sends a SPI command plus data payload to the LoRa chip
void LoRa_WriteCommand(uint8_t cmd, uint8_t *data, uint16_t len) {
    LoRa_WaitWhileBusy();                            // Wait until module is ready
    LoRa_Select();                                   // Activate NSS
    HAL_SPI_Transmit(&hspi2, &cmd, 1, 100);          // Send the command byte
    HAL_SPI_Transmit(&hspi2, data, len, 100);        // Send any extra data bytes
    LoRa_Unselect();                                 // Deactivate NSS
    LoRa_WaitWhileBusy();                            // Wait again for completion
}

// Puts LoRa module in standby (must be done before config/tx)
void LoRa_SetStandby(void) {
    uint8_t cmd[] = { 0x01, 0x00 }; // 0x01 = STDBY_RC mode
    LoRa_WriteCommand(0x80, cmd, sizeof(cmd)); // 0x80 = SetStandby command
}

// Tells LoRa to use LoRa modulation (not FSK)
void LoRa_SetPacketType(void) {
    uint8_t cmd[] = { 0x01 }; // 0x01 = LoRa packet
    LoRa_WriteCommand(0x8A, cmd, sizeof(cmd)); // 0x8A = SetPacketType
}

// Sets carrier frequency for LoRa (e.g., 868 MHz for Europe)
void LoRa_SetRfFrequency(uint32_t frequency_hz) {
    // Convert freq to SX1262 register format
    uint32_t freq = (uint32_t)((double)frequency_hz / 32000000.0 * (1 << 25));
    uint8_t cmd[] = {
        (uint8_t)(freq >> 24),
        (uint8_t)(freq >> 16),
        (uint8_t)(freq >> 8),
        (uint8_t)(freq >> 0)
    };
    LoRa_WriteCommand(0x86, cmd, sizeof(cmd)); // 0x86 = SetRfFrequency
}

// Sets output power and ramp time for transmission
void LoRa_SetTxParams(void) {
    uint8_t cmd[] = { 0x0F, 0xE0 }; // 0x0F = 15dBm, 0xE0 = 200us ramp
    LoRa_WriteCommand(0x8E, cmd, sizeof(cmd)); // 0x8E = SetTxParams
}

// Set buffer base address for both TX and RX (usually both 0)
void LoRa_SetBufferBaseAddress(void) {
    uint8_t cmd[] = { 0x00, 0x00 }; // TX base = 0x00, RX base = 0x00
    LoRa_WriteCommand(0x8F, cmd, sizeof(cmd)); // 0x8F = SetBufferBaseAddress
}

// Sets spreading factor, bandwidth, and coding rate (standard LoRa: SF7, 125kHz, CR4/5)
void LoRa_SetModulationParams(void) {
    uint8_t cmd[] = { 0x07, 0x04, 0x01 }; // SF7, 125kHz, CR 4/5
    LoRa_WriteCommand(0x8B, cmd, sizeof(cmd)); // 0x8B = SetModulationParams
}

// Packet parameters: preamble, explicit header, payload length, no CRC, standard IQ
void LoRa_SetPacketParams(uint8_t payload_length) {
    uint8_t cmd[] = { 0x08, 0x00, payload_length, 0x00, 0x00 };
    LoRa_WriteCommand(0x8C, cmd, sizeof(cmd)); // 0x8C = SetPacketParams
}

// Sends a string as LoRa packet (all config done each time for safety)
void LoRa_SendString(const char* str) {
    uint8_t buffer[256];                                  // Holds transmit payload
    uint8_t length = strlen(str);                         // Actual length of message
    buffer[0] = 0x00;                                     // Offset for write buffer
    memcpy(&buffer[1], str, length);                      // Copy message into buffer (after offset byte)

    LoRa_SetStandby();                                    // Required before configuration
    LoRa_SetPacketType();                                 // Use LoRa modulation
    LoRa_SetRfFrequency(868000000);                       // Set frequency to 868 MHz (EU)
    LoRa_SetBufferBaseAddress();                          // Start buffer at 0
    LoRa_SetModulationParams();                           // SF7/125kHz/CR4/5
    LoRa_SetPacketParams(length);                         // Set payload length
    LoRa_SetTxParams();                                   // Set TX power, ramp

    LoRa_WriteCommand(0x0E, buffer, length + 1);          // 0x0E = WriteBuffer command

    uint8_t tx_cmd[] = { 0x00, 0x00 };                    // Trigger TX, 0 = blocking
    LoRa_WriteCommand(0x83, tx_cmd, sizeof(tx_cmd));      // 0x83 = SetTx

    printf("LoRa: Sent '%s'\r\n", str);                   // Print confirmation to UART
}


// Reads the status register from the SX126x LoRa radio via SPI.
// This sends the GetStatus command (0xC0) and retrieves the status byte.
// uint8_t  The status byte read from the LoRa module.
uint8_t LoRa_GetStatus(void) {
    uint8_t cmd = 0xC0;       // 0xC0 is the GetStatus command for SX126x
    uint8_t dummy = 0x00;     // Dummy byte, required for clocking data out
    uint8_t status = 0;       // Variable to hold the status returned

    LoRa_Select();                                            // Pull NSS (chip select) low to begin SPI transaction
    HAL_SPI_Transmit(&hspi2, &cmd, 1, HAL_MAX_DELAY);         // Send the GetStatus command (0xC0)
    HAL_SPI_Transmit(&hspi2, &dummy, 1, HAL_MAX_DELAY);       // Send a dummy byte (required by SX126x protocol)
    HAL_SPI_Receive(&hspi2, &status, 1, HAL_MAX_DELAY);       // Receive the status byte from the LoRa module
    LoRa_Unselect();                                          // Pull NSS (chip select) high to end SPI transaction

    return status;                                            // Return the received status byte
}




/* ============================================================================
 * Initializes the BME680 sensor over I2C and sets config
 * ========================================================================= */
void BME680_Init(void) {
    bme.intf = BME68X_I2C_INTF;                   // Use I2C
    bme.read = user_i2c_read;                     // Attach I2C read callback
    bme.write = user_i2c_write;                   // Attach I2C write callback
    bme.delay_us = user_delay_us;                 // Attach delay callback
    bme.intf_ptr = &hi2c1;                        // Give Bosch API the I2C handle

    if (bme68x_init(&bme) != BME68X_OK) {         // Initialize sensor, fail if not OK
        printf("BME680 init failed!\r\n");
        while (1);
    }
    conf.os_temp = BME68X_OS_8X;                  // Set temperature oversampling (8x)
    conf.os_pres = BME68X_OS_4X;                  // Set pressure oversampling (4x)
    conf.os_hum = BME68X_OS_2X;                   // Set humidity oversampling (2x)
    conf.filter = BME68X_FILTER_SIZE_3;           // Set IIR filter size
    conf.odr = BME68X_ODR_NONE;                   // No periodic output
    bme68x_set_conf(&conf, &bme);                 // Send config to sensor

    heatr_conf.enable = BME68X_ENABLE;            // Turn on gas heater
    heatr_conf.heatr_temp = 320;                  // Heater temp (°C)
    heatr_conf.heatr_dur = 150;                   // Heater duration (ms)
    bme68x_set_heatr_conf(BME68X_FORCED_MODE, &heatr_conf, &bme); // Set heater

    printf("BME680 Initialized.\r\n");            // Confirmation on UART
}





